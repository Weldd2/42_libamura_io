#include "io.h"

void	io_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		io_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	c = n + '0';
	write(fd, &c, 1);
}
